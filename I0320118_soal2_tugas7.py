import math
a = 123.456
b = 345.678
c = 225.0
#fungsi ceil
print("Hasil pembulatan keatas a adalah: ",math.ceil(a))
print("Hasil pembulatan keatas b adalah: ",math.ceil(b))

#fungsi min dan max
print("nilai maksimumnya adalah= ", max(a,b,c))
print("nilai maksimumnya adalah= ", max(a,b,c))

#fungsi sqrt
print("nilai akar dari a adalah= ", math.sqrt(a))
print("nilai akar dari b adalah= ", math.sqrt(b))
print("nilai akar dari c adalah= ", math.sqrt(c))

#fungsi mathfloor
print("hasil pembulatan kebawah a adalah= ",math.floor(a))
print("hasil pembulatan kebawah b adalah= ",math.floor(b))