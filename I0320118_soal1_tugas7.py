# fungsi metode Capitalize
string = "saya senang mempelajari aplikasi pemograman Python"
s = string.capitalize()
print(s)

# fungsi metode upper dan lower
string2 = string.upper()
string3 = string.lower()
print(string2)
print(string3)

#fungsi metode replace
print(string.replace("senang", "sulit"))

#fungsi metode count
print("jumlah huruf s= ", string.count('s'))
print("jumlah huruf a= ", string.count('a'))